package com.example.demo.Utils;

public class MathUtils {
    int sum(int a, int b) {
        return a + b;
    }

    int subtract(int a, int b) {
        return a - b;
    }

    int multiply(int a, int b) {
        return a * b;
    }
    int divide(int a, int b) {
        return a / b;
    }
    int getRemainder(int a, int b) {
        return a % b;
    }
}
